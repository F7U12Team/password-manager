    private final javax.net.ssl.SSLSocketFactory mSSLSocketFactory;

    public SecureSSLSocketFactory() throws KeyManagementException,
            UnrecoverableKeyException, NoSuchAlgorithmException,
            KeyStoreException, CertificateException, IOException {

        super((KeyStore)null);

        KeyStore keyStore = loadKeyStore();

        final TrustManagerFactory tmf = TrustManagerFactory.getInstance("X509");
        tmf.init(keyStore);
        TrustManager[] trustManagers = tmf.getTrustManagers();

        SSLContext sslContext = SSLContext.getInstance(SSLSocketFactory.TLS);
        sslContext.init(null, trustManagers, null);
        mSSLSocketFactory = sslContext.getSocketFactory();
    }

    @Override
    public Socket createSocket() throws IOException {
        Socket socket = mSSLSocketFactory.createSocket();
        setSSLParameters((SSLSocket)socket);
        return socket;
    }

    @Override
    public Socket connectSocket(final Socket socket, final String host,
            final int port, final InetAddress localAddress, int localPort,
            final HttpParams params) throws IOException {
        final SSLSocket sslSocket = (SSLSocket) ((socket != null) ? socket
                : createSocket());
        setSSLParameters(sslSocket);
        if ((localAddress != null) || (localPort > 0)) {
            if (localPort < 0) {
                localPort = 0;
            }
            sslSocket.bind(new InetSocketAddress(localAddress, localPort));
        }
        final int connTimeout = HttpConnectionParams
                .getConnectionTimeout(params);
        final int soTimeout = HttpConnectionParams.getSoTimeout(params);
        final InetSocketAddress remoteAddress = new InetSocketAddress(host,
                port);
        sslSocket.connect(remoteAddress, connTimeout);
        sslSocket.setSoTimeout(soTimeout);

        try {
            SSLSocketFactory.STRICT_HOSTNAME_VERIFIER.verify(host, sslSocket);
        } catch (IOException iox) {
            try {
                sslSocket.close();
            } catch (Exception ignored) {
            }
            throw iox;
        }
        return sslSocket;
    }

    @Override
    public Socket createSocket(final Socket socket, final String host,
            int port, final boolean autoClose) throws IOException {
        if (port == -1) {
            port = 443;
        }

        // Doesn't perform SSL handshake
        final SSLSocket sslSocket = (SSLSocket) mSSLSocketFactory.createSocket(
                socket, host, port, autoClose);
        setSSLParameters(sslSocket);

        try {
            // Initiates the SSL handshake
            SSLSocketFactory.STRICT_HOSTNAME_VERIFIER.verify(host, sslSocket);
        } catch (IOException iox) {
            try {
                sslSocket.close();
            } catch (Exception ignored) {
            }
            throw iox;
        }
        return sslSocket;
    }

    @Override
    public void setHostnameVerifier(X509HostnameVerifier hostnameVerifier) {
      throw new IllegalArgumentException(
              "Only strict hostname verification (default) is supported!");
    }

    @Override
    public X509HostnameVerifier getHostnameVerifier() {
      return SSLSocketFactory.STRICT_HOSTNAME_VERIFIER;
    }

    private void setSSLParameters(SSLSocket socket) throws SSLException {
        // Set TLS protocol version
        List<String> protocols = Arrays.asList(socket.getSupportedProtocols());
        if (protocols.contains("TLSv1.2")) {
            socket.setEnabledProtocols(new String[] { "TLSv1.2" });
        } else if (protocols.contains("TLSv1.0")) {
            socket.setEnabledProtocols(new String[] { "TLSv1.0" });
        } else if (protocols.contains("TLSv1")) {
            socket.setEnabledProtocols(new String[] { "TLSv1" });
        } else {
            throw new SSLException("Minimal TLS version 1 support required!");
        }
        String[] supportedCipherSuites = socket.getSupportedCipherSuites();
        List<String> allowedCipherSuitesList = new ArrayList<String>(
                Arrays.asList(sAllowedCipherSuites));
        List<String> supportedCipherSuitesList = Arrays
                .asList(supportedCipherSuites);
        allowedCipherSuitesList.retainAll(supportedCipherSuitesList);
        String[] enabledCipherSuites = allowedCipherSuitesList
                .toArray(new String[allowedCipherSuitesList.size()]);
        socket.setEnabledCipherSuites(enabledCipherSuites);
    }

    private KeyStore loadKeyStore() throws KeyStoreException,
            NoSuchAlgorithmException, CertificateException, IOException {
        InputStream is = new ByteArrayInputStream(sRootKeyStoreBytes);
        final KeyStore trustStore = KeyStore.getInstance("BKS");
        trustStore.load(is, null);
        is.close();
        return trustStore;
    }

