    /**
     * Joins multiple chunks into a single byte array
     */
    private static final byte[] GetRootKeyStoreBytes(){
        byte[][] chunks = new byte[][] {GetRootKeyStoreBytes1(), GetRootKeyStoreBytes2(), GetRootKeyStoreBytes3()};

        int totalLength = 0;
        for(byte[] chunk : chunks) {
            totalLength += chunk.length;
        }

        byte[] data = new byte[totalLength];
        int i = 0;
        for(byte[] chunk : chunks) {
            for(byte b : chunk) {
                data[i] = b;
                i++;
            }
        }
        return data;
    }

